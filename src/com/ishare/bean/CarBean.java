package com.ishare.bean;

// nullable
public class CarBean {

	private long id;
	
	private String paizhao;
	
	private String type;
	
	private String taxi_company;
	
	private String employee_num;
	
	private String employee_identification_pic;
	
	private String status;
	
	private String driving_license_front;
	
	private String driving_license_back;

	public String getPaizhao() {
		return paizhao;
	}

	public void setPaizhao(String paizhao) {
		this.paizhao = paizhao;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getTaxi_company() {
		return taxi_company;
	}

	public void setTaxi_company(String taxi_company) {
		this.taxi_company = taxi_company;
	}

	public String getEmployee_num() {
		return employee_num;
	}

	public void setEmployee_num(String employee_num) {
		this.employee_num = employee_num;
	}

	public String getEmployee_identification_pic() {
		return employee_identification_pic;
	}

	public void setEmployee_identification_pic(String employee_identification_pic) {
		this.employee_identification_pic = employee_identification_pic;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getDriving_license_front() {
		return driving_license_front;
	}

	public void setDriving_license_front(String driving_license_front) {
		this.driving_license_front = driving_license_front;
	}

	public String getDriving_license_back() {
		return driving_license_back;
	}

	public void setDriving_license_back(String driving_license_back) {
		this.driving_license_back = driving_license_back;
	}

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}
}
