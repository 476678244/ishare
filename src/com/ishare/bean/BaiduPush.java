package com.ishare.bean;

public class BaiduPush {

	private long userId;

	private String baiduUser;

	private String baiduChannel;

	public long getUserId() {
		return userId;
	}

	public void setUserId(long userId) {
		this.userId = userId;
	}

	public String getBaiduUser() {
		return baiduUser;
	}

	public void setBaiduUser(String baiduUser) {
		this.baiduUser = baiduUser;
	}

	public String getBaiduChannel() {
		return baiduChannel;
	}

	public void setBaiduChannel(String baiduChannel) {
		this.baiduChannel = baiduChannel;
	}
}
